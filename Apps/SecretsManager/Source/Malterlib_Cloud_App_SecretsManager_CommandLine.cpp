#include <Mib/Core/Core>
#include <Mib/Encoding/JsonShortcuts>

#include "Malterlib_Cloud_App_SecretsManager.h"
#include "Malterlib_Cloud_App_SecretsManager_Server.h"

namespace NMib::NCloud::NSecretsManager
{
	void CSecretsManagerDaemonActor::fp_BuildCommandLine(CDistributedAppCommandLineSpecification &o_CommandLine)
	{
		CDistributedAppActor::fp_BuildCommandLine(o_CommandLine);

		o_CommandLine.f_SetProgramDescription
			(
				"Malterlib Cloud Secrets Manager"
				, "Manages secrets for Malterlib cloud apps."
			)
		;

		[[maybe_unused]] auto DefaultSection = o_CommandLine.f_GetDefaultSection();
	}
}
